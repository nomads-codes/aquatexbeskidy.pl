import { keyframes, css } from 'styled-components';

// ─────────────────────────────────────────────────────────────────────────────
// Component
// ─────────────────────────────────────────────────────────────────────────────

export const animationKeyframes = ({ from = {}, to = {}, properties = '' } = {}) => {
  const setup = keyframes`
    from {${from}}
    to   {${to}}
  `;

  return css`
    ${setup} ${properties}
  `;
};
